package main

import "fmt"

func main() {
	// Deklarasi variable

	// Pertama
	var name string

	name = "Dwi Karma"
	fmt.Println(name)

	name = "Made Karma"
	fmt.Println(name)

	// Kedua
	var namateman = "Sadako"
	fmt.Println(namateman)

	var umur = 38
	fmt.Println(umur)

	// Ketiga
	nama := "MadeKarma"
	fmt.Println(nama)

	nama = "DwiKarma"
	fmt.Println(nama)

	// Keempat
	var (
		firstname = "Putu"
		lastname  = "Yoeliessya"
	)

	fmt.Println(firstname)
	fmt.Println(lastname)
}
